        <p class="hoofdtext"><b>SHREC (SHifting towards Renewable Energy for Transition to Low Carbon Energy)</b><br>Europa wil de emissie van broeikasgassen terugdringen. Dat vraagt om versnelling en intensivering van strategieën die bijdragen aan het verduurzamen van de huidige energiesystemen. Een grote opgave ligt in de Europese regio's: de energietransitie krijgt concreet vorm via technologische vernieuwing en sociale innovaties. Burgerinitiatieven zoals energie coöperaties gaan een steeds belangrijker rol spelen. 
SHREC onderzoekt hoe de rol van communities, coöperaties en andere initiatieven van burgers en (MKB) bedrijven kan worden vergroot en versterkt naast de sociale acceptatie van nieuwe technologieën in de energietransitie. De Hanzehogeschool werkt samen met instellingen uit Zweden, Litouwen, Roemenië, Italië, Slowakije, Spanje en Frankrijk.<br><br><b>InnoHEIs (from Fragmented to Integrated and Sustainable Cooperation)</b><br>Dit project is gericht op het vergroten van de rol voor hoger onderwijsinstellingen op het gebied van regionale innovatieontwikkeling. Het bevorderen van ondernemerschap en creativiteit levert de regio's veel voordelen op. Deelnemers uit verschillende omgevingen zoals beleid, bedrijfsleven en academische wereld ontdekken op deze wijze potentiële nieuwe activiteiten en kansen om regionale maatschappelijke uitdagingen op te lossen. De Hanzehogeschool werkt samen met SNN en partners uit Spanje, Frankrijk, Finland, Zweden Litouwen en Polen binnen dit meerjarige project.

De Hanzehogeschool Groningen stimuleert in allerlei opzichten dat haar studenten en medewerkers meedenken aan antwoorden en oplossingen voor en met de regio. Daarin wordt nauw samengewerkt met kleine en grote bedrijven, organisaties en instellingen, overheden en burgers. Deze twee projecten betekenen concreet dat de hogeschool de komende vijf jaren vooruit kan met de ingeslagen weg met projecten als EnTranCe, Gebiedscoöperaties, Innovatiewerkplaatsen en hubs.<br><br><i>Bron artikel: <a href="https://www.hanze.nl" target="_blank">hanze.nl</a><br>
