Greenfield

Avebe vraagt studenten om vernieuwende ideeën aan te dragen, vanuit een greenfield principe: wat als je een fabriek helemaal opnieuw mocht bouwen? Welke technieken en welke volgorde zou je dan toepassen om de ingrediënten uit de aardappel maximaal tot waarde te brengen, met minimale inzet van energie, water en chemie?

De inschrijving is inmiddels geopend op https://www.avebe.nl/student-challenge/ en sluit op 9 september. Voorstellen kunnen tot 30 oktober 2019 worden ingediend. Vervolgens beoordeelt een deskundige jury alle ideeën. De drie beste inzendingen worden uitgenodigd om hun plan te presenteren in het nieuwe Innovatiecentrum in Groningen tijdens de Avebe Jubileumdag in november. Op die dag maakt Avebe ook de winnaar(s) bekend van een jaar collegegeld.
Over Avebe

Avebe is een coöperatie van 2.300 akkerbouwers uit Nederland en Duitsland. De zetmeelaardappelen van deze leden worden jaarlijks verwerkt tot hoogwaardige ingrediënten op basis van aardappelzetmeel en –eiwit, waar ze wereldwijd toegevoegde waarde bieden in voeding, maar ook in industriële toepassingen. Avebe werkt voortdurend aan de ontwikkeling van nieuwe mogelijkheden en toepassingen op basis van zetmeelaardappelen en richt zich op duurzame continuïteit. Het bedrijf heeft 1.300 medewerkers en productielocaties in Nederland, Duitsland en Zweden en heeft verkoopkantoren in de Verenigde Staten, Europa, Azië en het Midden- Oosten. Het hoofdkantoor staat in Veendam. Op Campus Groningen heeft Avebe in 2018 een nieuw Innovatiecentrum geopend. <br><br>Bron: Avebe
