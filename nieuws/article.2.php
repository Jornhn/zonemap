        <br><br><b>Alliantie Rookvrij Nederland</b><br>Sinds april 2017 is de Hanze verbonden aan Alliantie Rookvrij Nederland. De Hanze werkt binnen de gemeente Groningen samen met zo'n 40 andere organisaties aan het rookvrij maken van organisaties, zoals het UMCG, RUG, SKSG, Healthy Ageing Network Noord-Nederland. Als kennisinstelling en grote werkgever heeft de Hanze een voorbeeldfunctie als het gaat om gezond gedrag. Daarom willen zij een organisatie zijn die maximaal bijdraagt aan de preventie van roken en het stoppen-met-roken.

Onderdeel van dit beleid is een rookvrije Hanze. Zij zijn een schakel binnen de Alliantie Rookvrij Nederland: de Hanzestudenten komen in de toekomst vanuit een rookvrije omgeving naar de Hanze: kinderdagverblijven, basisscholen, sportverenigingen, maar ook voorgezet onderwijs en het MBO worden of zijn al rookvrij.</p>

